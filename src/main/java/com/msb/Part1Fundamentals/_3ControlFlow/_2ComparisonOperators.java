package com.msb.Part1Fundamentals._3ControlFlow;

public class _2ComparisonOperators {
    public static void main(String[] args) {
        int x = 1;
        int y = 1;
        System.out.println(x == y); // true
        System.out.println(x != y); // false
        System.out.println(x > y); // false
        System.out.println(x >= y); // true
        System.out.println(x < y); // false
        System.out.println(x <= y); // true
    }
}
